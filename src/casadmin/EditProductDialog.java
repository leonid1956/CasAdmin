    public EditProductDialog(java.awt.Frame parent, 
                                boolean modal,
                                Vector p_tableRow,
                                boolean p_bInsertOrEdit) {
        super(parent, modal);
        initComponents();
        // Wypełniamy wszystkie ComboBox'y, wykorzystując statyczne zmienne
        // klasy NewUserPanel
        //Wypełniamy comboBox cbProduct ze żródła NewUserPanel.vProducts
        cbProduct.removeAllItems();
        Iterator it = NewUserPanel.vProducts.iterator();
        while (it.hasNext()) {
             cbProduct.addItem(it.next());
        }
        //Wypełniamy comboBox cbPlatnik ze żródła NewUserPanel.mapPlatnik.keySet()
        cbPlatnik.removeAllItems();
        it = NewUserPanel.mapPlatnik.keySet().iterator();
        while (it.hasNext()) {
             cbPlatnik.addItem(it.next());
        }
        //Wypełniamy comboBox cbPlatnosc ze żródła NewUserPanel.mapPlatnosc.keySet()
        cbPlatnosc.removeAllItems();
        it = NewUserPanel.mapPlatnosc.keySet().iterator();
        while (it.hasNext()) {
             cbPlatnosc.addItem(it.next());
        }
        //Wypełniamy comboBox cbEkspres, cbDrobnica ze żródła NewUserPanel.mapPrices.keySet()
        cbEkspres.removeAllItems();
        cbDrobnica.removeAllItems();
        it = NewUserPanel.mapPrices.keySet().iterator();
        while (it.hasNext()) {
             String next = (String)it.next();
             cbEkspres.addItem(next);
             cbDrobnica.addItem(next);
        }
        //Wypełniamy comboBox cbPalety ze żródła NewUserPanel.mapYesNo.keySet()
        cbPalety.removeAllItems();
        it = NewUserPanel.mapYesNo.keySet().iterator();
        while (it.hasNext()) {
             cbPalety.addItem(it.next());
        }

        if (p_tableRow == null)
        {
                // Jeśli przekazany wektor ma wartość "null" - znaczy,
                // że chcemy dodać produkt do listy produktów
                this.setTitle("Dodanie nowego produktu");
        } else {
                // Inaczej czcemy modyfikować zawartość przesyłanych zmiennych.
                // Wybieramy bieżące znaczenia z ComboBox'ów
                cbProduct.setSelectedItem(p_tableRow.elementAt(0));
                cbPlatnik.setSelectedItem(p_tableRow.elementAt(1));
                cbPlatnosc.setSelectedItem(p_tableRow.elementAt(2));
                cbEkspres.setSelectedItem(p_tableRow.elementAt(3));
                cbDrobnica.setSelectedItem(p_tableRow.elementAt(4));
                cbPalety.setSelectedItem(p_tableRow.elementAt(5));
                this.setTitle("Edycja istniejącego produktu");
        }
        // Przy edycji nie pozwalamy zmieniać kluczowych pól
        cbProduct.setEnabled(p_bInsertOrEdit);
        cbPlatnik.setEnabled(p_bInsertOrEdit);
        cbPlatnosc.setEnabled(p_bInsertOrEdit);
        // Program oczekuje na wybieranie nowych wartości w comboBox'ach i
        // zatwierdzenie lub cofanie się.
    }

