    private void doClose(int retStatus) {
        if (retStatus == RET_OK)
        {
            // Użytkownik nacisnął "OK".
            // Przygotowyjemy wektor, który będzie dopisywany lub zamieści inny
            // wektor w tablice
            retVector = new Vector();
            retVector.add(cbProduct.getSelectedItem());
            retVector.add(cbPlatnik.getSelectedItem());
            retVector.add(cbPlatnosc.getSelectedItem());
            retVector.add(cbEkspres.getSelectedItem());
            retVector.add(cbDrobnica.getSelectedItem());
            retVector.add(cbPalety.getSelectedItem());
        }
        // Zamykamy dialog
        returnStatus = retStatus;
        setVisible(false);
        dispose();
    }

    /**  zwraca informacje o przycisku OK czy Cofnij nacisnął użytkownik
    */
    public int getReturnStatus() {
        // zmienna returnStatus jest prywatna, robimy publiczną metodę,
        // żeby zwrócić informację który przycisk (Ok czy Cofnij) nacisnął
        // użytkownik
        return returnStatus;
    }



