    private void doClose(int retStatus) {
        if (retStatus == RET_OK)
        {
            // Użytkownik nacisnął "OK".
            retDepot = tfDepot.getText();
            retIdCust = tfIdCust.getText();
            retDepotDR = tfDepotDR.getText();
            if (!YPFunctions.isInteger(retIdCust))
            {
                YPFunctions.showErrorMessage("ID CUST powinien być cyfrowy");
                return;
            }
            if ((retIdCust.length() < 4) || (retIdCust.length() > 6)) {
                YPFunctions.showErrorMessage("ID CUST powinien zawierać 4, 5 lub 6 cyfr");
                return;
            }
            if (retDepot.length() != 2) {
                YPFunctions.showErrorMessage("Nie prawidłowy depot");
                return;
            }
            if (depotDrExists) {
                if (retDepotDR.length() != 2) {
                    YPFunctions.showErrorMessage("Nie prawidłowy depot dla drobnicy");
                    return;
                }
            }
            
        }
        // Zamykamy dialog
        returnStatus = retStatus;
        setVisible(false);
        dispose();
    }

    /**  zwraca informacje o przycisku OK czy Cofnij nacisnął użytkownik
    */
    public int getReturnStatus() {
        // zmienna returnStatus jest prywatna, robimy publiczną metodę,
        // żeby zwrócić informację który przycisk (Ok czy Cofnij) nacisnął
        // użytkownik
        return returnStatus;
    }



